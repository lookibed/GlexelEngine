local gdi = require("gdi")

local M = {}

-- ============================================================
-- Basic primitives
-- ============================================================

function M.pixel(x, y, color)
  gdi.pixel(x, y, color)
end

function M.rect(x, y, w, h, color)
  gdi.rect(x, y, w, h, color)
end

function M.clear(color)
  gdi.clear(color)
end

function M.present()
  gdi.present()
end

function M.line(x0, y0, x1, y1, color)
  x0 = math.floor(x0)
  y0 = math.floor(y0)
  x1 = math.floor(x1)
  y1 = math.floor(y1)

  local dx = math.abs(x1 - x0)
  local sx = x0 < x1 and 1 or -1

  local dy = -math.abs(y1 - y0)
  local sy = y0 < y1 and 1 or -1

  local err = dx + dy

  while true do
    gdi.pixel(x0, y0, color)

    if x0 == x1 and y0 == y1 then
      break
    end

    local e2 = err * 2

    if e2 >= dy then
      err = err + dy
      x0 = x0 + sx
    end

    if e2 <= dx then
      err = err + dx
      y0 = y0 + sy
    end
  end
end

function M.rect_outline(x, y, w, h, color)
  M.line(x, y, x + w, y, color)
  M.line(x + w, y, x + w, y + h, color)
  M.line(x + w, y + h, x, y + h, color)
  M.line(x, y + h, x, y, color)
end

function M.circle(cx, cy, radius, color)
  cx = math.floor(cx)
  cy = math.floor(cy)
  radius = math.floor(radius)

  local x = radius
  local y = 0
  local err = 0

  while x >= y do
    gdi.pixel(cx + x, cy + y, color)
    gdi.pixel(cx + y, cy + x, color)
    gdi.pixel(cx - y, cy + x, color)
    gdi.pixel(cx - x, cy + y, color)
    gdi.pixel(cx - x, cy - y, color)
    gdi.pixel(cx - y, cy - x, color)
    gdi.pixel(cx + y, cy - x, color)
    gdi.pixel(cx + x, cy - y, color)

    y = y + 1

    if err <= 0 then
      err = err + 2 * y + 1
    end

    if err > 0 then
      x = x - 1
      err = err - 2 * x + 1
    end
  end
end

function M.circle_filled(cx, cy, radius, color)
  cx = math.floor(cx)
  cy = math.floor(cy)
  radius = math.floor(radius)

  for y = -radius, radius do
    local xx = math.floor(math.sqrt(radius * radius - y * y))
    gdi.rect(cx - xx, cy + y, xx * 2 + 1, 1, color)
  end
end

