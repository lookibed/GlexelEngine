function M.char(ch, x, y, color, scale)
  scale = scale or 1

  ch = string.upper(ch)
  local glyph = font[ch] or font[" "]

  for gy = 1, 7 do
    local row = glyph[gy]

    for gx = 1, 5 do
      if row:sub(gx, gx) == "1" then
        gdi.rect(
          x + (gx - 1) * scale,
          y + (gy - 1) * scale,
          scale,
          scale,
          color
        )
      end
    end
  end
end

function M.text(text, x, y, color, scale)
  scale = scale or 1

  local cursor_x = x

  for i = 1, #text do
    local ch = text:sub(i, i)
    M.char(ch, cursor_x, y, color, scale)
    cursor_x = cursor_x + 6 * scale
  end
end

return M
